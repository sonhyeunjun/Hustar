package cal;

public class Cal {
	private int calID;
	private String cmContents;
	private int cmYear;
	private int cmMonth;
	private int cmDay;

	
	public int getCalID() {
		return calID;
	}

	public void setCalID(int calID) {
		this.calID = calID;
	}

	public String getCmContents() {
		return cmContents;
	}

	public void setCmContents(String cmContents) {
		this.cmContents = cmContents;
	}

	public int getCmYear() {
		return cmYear;
	}

	public void setCmYear(int cmYear) {
		this.cmYear = cmYear;
	}

	public int getCmMonth() {
		return cmMonth;
	}

	public void setCmMonth(int cmMonth) {
		this.cmMonth = cmMonth;
	}

	public int getCmDay() {
		return cmDay;
	}

	public void setCmDay(int cmDay) {
		this.cmDay = cmDay;
	}

}
